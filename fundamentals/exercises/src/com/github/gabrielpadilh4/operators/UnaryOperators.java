package com.github.gabrielpadilh4.operators;

public class UnaryOperators {

    public static void main(String[] args) {
        int a = 1;
        int b = 2;

        // Postfix
        a++; // a = a + 1;
        a--; // a = a - 1;

        // Prefix
        ++b; // b = b + 1;
        --b; // b = b - 1;
    }

}
