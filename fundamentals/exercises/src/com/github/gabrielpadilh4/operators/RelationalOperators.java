package com.github.gabrielpadilh4.operators;

public class RelationalOperators {

    public static void main(String[] args) {
        int a = 97;
        int b = 'a'; // unicode

        System.out.println(a == b);

        System.out.println(3 >= 4);

        System.out.println(3 < 7);

        System.out.println(30 <= 7);
    }
}
